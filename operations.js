// variables
var apples = 10;
var oranges = 20;
var rottenOranges = 6;
var perFruits = 5;
var kids = 6;


// Addition and Subtraction

var totalFruits = apples + oranges;
console.log(apples + oranges);
console.log(totalFruits);


var freshOranges = oranges - rottenOranges;
console.log(oranges - rottenOranges);
console.log(freshOranges);

// Multiplication

var totalPrice = totalFruits * perFruits;
console.log(totalPrice);
// Division

var fruitsPerKids = totalFruits / kids;
console.log(fruitsPerKids);