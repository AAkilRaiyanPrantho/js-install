// Variable Declare
var price = 50;

// Variable Update
price = 60;

// Add something to the previous value
var price1 = 31;
var price2 = 10;

// set a new value 
// price1 = 35;

// add some value to the existing value
price1 = price1 + 10;
console.log(price1);

// addition to the same variable shorthand
//  += means adding some value to the existing value
price1 += 10;
console.log(price1);

// subtracting from the same value
price2 = price2 - 5;
console.log(price2);
// shorthand -= means deducting some values from the current value
price -= 5;
console.log(price1);

// Multiplication and Division
// *=
price1 = price1 *5;
console.log(price1);
price1 *= 5;
console.log(price1);

// /=

price1 = price1 / 2;
console.log(price1); 
price1 /= 2; 
console.log(price1);


// Increment and Decrement shorthand

var age = 25;
console.log(age);

// Increases the existing value by one(1)
age++;
console.log(age);
// Decreases the existing value by one(1)
age--;
console.log(age);

