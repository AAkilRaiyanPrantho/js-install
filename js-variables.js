// vary + able = variable
// keyword name equal-sign(=) value semicolon(;)

// Numeric
var price = 95;
var temperature = 36.5;
var age = 25;

var weight = 83.7;
var money = 25000;
var height = 180.3;

// String or Character type

var name = "aakil";
var place = 'dhaka';

// Boolean

var islam = true;
var others = false;


// Variable Name Do's and Dont's

// Rule 1: Variable Name must be in a single word

var area = 51;

// Rule 2: Variable Name cannot start with a number but can end with a number

var area51 = "aliens";

// Rule 3: Variable Names are case sensitive

var person = "Kamal";
var Person = "Karim";
// These two are two different variable even though they are same words

// Rule 4: Cannot use Keywords as variable names
// var for = true;

// Rule 5: In case of a long and multi-word name, we can use one of the three rules -

